package com.cutezilla.cryptomanager.model;

public class AllCoin {
    String symbol;
    String name;
    String image;
    String current_price;

    public AllCoin() {
    }

    public String getSymbol() {
        return symbol;
    }

    public String getName() {
        return name;
    }

    public String getImage() {
        return image;
    }

    public String getCurrent_price() {
        return current_price;
    }
}
